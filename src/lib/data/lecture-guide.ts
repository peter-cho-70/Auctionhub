const GUIDE: Record<CaseStatus, string> = {} as Record<CaseStatus, string>;
for (const step of Object.keys(LECTURE_BLOCKS_BY_STEP) as CaseStatus[]) {
  GUIDE[step] = joinBlocks(LECTURE_BLOCKS_BY_STEP[step]);
}

/** 단계별 블록 목록 (학습 미리보기 등) */
export function getDefaultLectureBlocksForStep(
  step: CaseStatus,
): LectureGuideBlock[] {
  return LECTURE_BLOCKS_BY_STEP[step] ?? [];
}

/** 앱에 저장된 편집본이 없을 때 사용하는 기본 강의 노트 */
export function getDefaultLectureGuideForStep(step: CaseStatus): string {
  return GUIDE[step] ?? "";
}

/** 저장된 편집본이 있으면 우선, 없으면 기본 노트 */
export function getResolvedLectureGuide(
  step: CaseStatus,
  overrides: Partial<Record<CaseStatus, string>> | undefined,
): string {
  const o = overrides?.[step];
  if (o !== undefined) return o;
  return getDefaultLectureGuideForStep(step);
}

/** @deprecated getResolvedLectureGuide 사용 */
export function getLectureGuideForStep(step: CaseStatus): string {
  return getDefaultLectureGuideForStep(step);
}
